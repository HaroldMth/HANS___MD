const EMOJI_COUNT = EMOJIS.length;

/**
 * Pick a random emoji from the database.
 */
function pickEmoji() {
  return EMOJIS[Math.floor(Math.random() * EMOJI_COUNT)];
}

/**
 * Send an auto-reaction to a message.
 * Respects config.AUTO_REACT — no-op if disabled.
 */
async function autoReact(conn, mek) {
  if (!config.AUTO_REACT) return;
  try {
    await conn.sendMessage(mek.key.remoteJid, {
      react: { text: pickEmoji(), key: mek.key }
    });
  } catch {
    // silently ignore — reactions are best-effort
  }
}

module.exports = { autoReact };
